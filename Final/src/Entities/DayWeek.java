package Entities;

public class DayWeek {
	private int idDayWeek;
	private String nameDayWeek;
	
	public DayWeek()
	{
		
	}

	public int getIdDayWeek() {
		return idDayWeek;
	}

	public void setIdDayWeek(int idDayWeek) {
		this.idDayWeek = idDayWeek;
	}

	public String getNameDayWeek() {
		return nameDayWeek;
	}

	public void setNameDayWeek(String nameDayWeek) {
		this.nameDayWeek = nameDayWeek;
	}
	
	
}
