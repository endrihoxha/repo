package Entities;

public class SubjectDataPojo {
	
	private Subject lenda;
	private Branch dega;
	private Year viti;
	
	public SubjectDataPojo()
	{
		
	}

	public Subject getLenda() {
		return lenda;
	}

	public void setLenda(Subject lenda) {
		this.lenda = lenda;
	}

	public Branch getDega() {
		return dega;
	}

	public void setDega(Branch dega) {
		this.dega = dega;
	}

	public Year getViti() {
		return viti;
	}

	public void setViti(Year viti) {
		this.viti = viti;
	}
}
