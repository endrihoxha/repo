package Entities;

public class Professor_Subject {

	private int idProfessorSubject;
	private int idProfessor; 
	private int idSubject;
	private int idSubjectType;
	public int getIdProfessorSubject() {
		return idProfessorSubject;
	}
	public void setIdProfessorSubject(int idProfessorSubject) {
		this.idProfessorSubject = idProfessorSubject;
	}
	public int getIdProfessor() {
		return idProfessor;
	}
	public void setIdProfessor(int idProfessor) {
		this.idProfessor = idProfessor;
	}
	public int getIdSubject() {
		return idSubject;
	}
	public void setIdSubject(int idSubject) {
		this.idSubject = idSubject;
	}
	public int getIdSubjectType() {
		return idSubjectType;
	}
	public void setIdSubjectType(int idSubjectType) {
		this.idSubjectType = idSubjectType;
	}
	
	
}
