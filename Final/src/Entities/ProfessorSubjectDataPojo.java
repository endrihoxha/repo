package Entities;

public class ProfessorSubjectDataPojo {
	
	private int idProfessor_Subject;
	private String subject;
	private String branch;
	private int year;
	private String type;
	
	
	
	public int getIdProfessor_Subject() {
		return idProfessor_Subject;
	}
	public void setIdProfessor_Subject(int idProfessor_Subject) {
		this.idProfessor_Subject = idProfessor_Subject;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getBranch() {
		return branch;
	}
	public void setBranch(String branch) {
		this.branch = branch;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}

	
	
}
